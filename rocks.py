import turtle
WIDTH , HEIGHT = 800, 1200
scr = turtle.Screen()
scr.setup(WIDTH, HEIGHT)


t = turtle.Turtle()
scr.tracer(0)
t.penup()


def frame(t: turtle.Turtle, WITDH, HEIGHT, color = "#000000"):
   t.goto(-350,-350)
   t.pendown()
   t.fillcolor("#DE3C45")
   t.begin_fill()
   t.forward(650)
   t.left(90)
   t.forward(800)
   t.left(90)
   t.forward(650)
   t.left(90)
   t.forward(800)
   t.end_fill()

t.penup()

def rolks(t: turtle.Turtle, WITDH, HEIGHT, color = "#000000"):
   t.goto(-350,-250)
   t.pendown()
   t.fillcolor("#321F10")
   t.begin_fill()
   t.left(90)
   t.forward(20)
   t.left(20)
   t.forward(10)
   t.right(20)
   t.forward(20)
   t.left(10)
   t.forward(50)
   t.left(45)
   t.forward(20)
   t.right(85)
   t.forward(40)
   t.left(45)
   t.left(70)
   t.forward(20)
   t.right(90)
   t.forward(50)
   t.right(75)
   t.forward(20)
   t.forward(40)
   t.left(75)
   t.penup()
   t.forward(75)
   t.pendown()
   t.circle(100,450)
   t.right(20)
   t.forward(70)
   t.right(40)
   t.forward(100)
   t.right(40)
   t.forward(50)
   t.right(20)
   t.forward(30)
   t.right(40)
   t.forward(70)
   t.left(80)
   t.forward(50)
   t.right(95)
   t.forward(160)
   t.right(90)
   t.forward(650)
   t.right(90)
   t.forward(100)
   t.end_fill

frame(t, 0,0)
rolks(t, 0,0)

scr.update()
turtle.done()